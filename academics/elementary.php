			<p>The elementary school at CAT’s fundamental belief is that a education should be focusing on the entire student and not just on the academics. Students at CAT are integral parts of their classroom, grade level, and school communities. To build this sense of community, elementary uses the Responsive Classroom approach to community building with logical consequences for discipline. To compliment this, CAT has also adopted the Olweus Program to help build positive relationships and prevent bullying. Elementary mothers also come once a month to help teachers fortify the core values that are instilled in the students by teaching values classes, focusing on specific topics such as respect, responsibility, etc. taught by mothers in Spanish.</p>
			<p>Academically, the elementary school balances comprehensive programs in both English and Spanish from grades 1 to 6. The English program is based on American benchmarks and standards adapted to fit the needs of our community while the Spanish program classes are based on the Mexican SEP (Secretary of Public Education) standards and curriculum. To develop a higher level of spoken English and consolidate essential skills in reading and writing, students in grades 1 through 3 spend the majority of their instructional time in English class, with Spanish classes 4.5 hours a week. In grades 4 through 6 students then expand upon their strong Spanish base by receiving instruction for half of the day in Spanish and half of the day in English. Both English and Spanish programs in all grades address the spoken and written aspects (writing, grammar, reading, oral language) of the language of instruction as well as math concepts. Science is taught only in English in grades 1 through 3 and Social Studies in both languages while both subjects are taught in both languages in grades 4 through 6. Students in grades 4 to 6 have the opportunity to explore the sciences with hands-on activities, labs and investigations twice a week, fulfilling science curricular requirements. Various other subjects are touched upon in Spanish to comply with SEP requirements. </p>
			<p>In the elementary school we believe that students learn best when they are working at their own level, have choice, and are actively engaged in their own learning process. For this reason, we use Everyday Math as our guide to math instruction. It encourages real-life problem solving using balanced instruction and multiple methods for learning the basic skills. Practice emphasizes an enhanced home and school communication to reinforce what is being taught in class.</p>
			<p>These basic beliefs in student learning permeate all aspects of learning. Literacy instruction in the English program is currently transitioning from the traditional method of teaching to a balanced literacy approach. Balanced literacy is an approach to instruction based on all of the current research in education. Students have a variety of literacy activities throughout the day including, but not limited to, independent reading, guided reading, shared reading, read alouds, book clubs, independent writing, shared writing, and word work. Spelling is taught through word work while reading writing skills, including grammar, are taught through the various reading and writing activities mentioned above. This gives teachers the means to meet individual student’s needs as each student travels his or her own educational paths. To address individual academic needs of students, CAT offers the Talented and Gifted (TAG) program and the Program for Extra Preparation (PEP). Students in grades 3-6 who meet specified requirements have the opportunity to participate in the school’s Talented and Gifted (TAG) program. The TAG classes are taught via a pull-out program by a designated teacher. Students attend their TAG class three times a week. The PEP program offers students with specific learning needs the extra support needed to be successful in the mainstream classroom. PEP is mostly a push in program, but also operates a pull out portion on a limited basis.</p>
			<p>Rounding out the student’s holistic educational experience, all students in the elementary school at have the opportunity to broaden their perspectives through the special classes that are offered during the regular school day. These classes include physical education, music, computer and library classes. Students in 5th and 6th grades have the option of taking Introduction to Strings, Introduction to Winds or Chorus as their music class. After school, there are a variety of sports and arts programs also offered ranging from dance to tae-kwon-doe to the ensemble.</p>
